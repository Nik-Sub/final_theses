package com.mobile.iwbi.presentation.theme

import androidx.compose.ui.unit.dp

// IWBI Design System Dimensions
object IWBIDimens {
    // Standard spacing scale
    val spaceXs = 4.dp      // 4dp - very small spacing
    val spaceS = 8.dp       // 8dp - small spacing
    val spaceM = 16.dp      // 16dp - medium spacing (most common)
    val spaceL = 24.dp      // 24dp - large spacing
    val spaceXl = 32.dp     // 32dp - extra large spacing
    val spaceXxl = 48.dp    // 48dp - extra extra large spacing

    // Content padding
    val contentPadding = spaceM          // 16dp - standard content padding
    val contentPaddingLarge = spaceL     // 24dp - large content padding
    val cardPadding = spaceM             // 16dp - card content padding
    val buttonPadding = spaceS           // 8dp - button internal padding

    // Component dimensions
    val buttonHeight = 48.dp             // Standard button height
    val buttonHeightSmall = 36.dp        // Small button height
    val textFieldHeight = 56.dp          // Standard text field height
    val iconSize = 24.dp                 // Standard icon size
    val iconSizeSmall = 16.dp            // Small icon size
    val iconSizeLarge = 48.dp            // Large icon size

    // Layout dimensions
    val bottomBarHeight = 64.dp          // Bottom navigation bar height
    val topAppBarHeight = 64.dp          // Top app bar height
    val fabSize = 56.dp                  // Floating action button size
    val fabSizeSmall = 40.dp             // Small floating action button size

    // Card and container dimensions
    val cardElevation = 2.dp             // Standard card elevation
    val cardElevationHigh = 8.dp         // High card elevation
    val cornerRadius = 12.dp             // Standard corner radius
    val cornerRadiusSmall = 8.dp         // Small corner radius
    val cornerRadiusLarge = 16.dp        // Large corner radius

    // Divider and border
    val dividerHeight = 1.dp             // Standard divider height
    val borderWidth = 1.dp               // Standard border width

    // List item dimensions
    val listItemHeight = 72.dp           // Standard list item height
    val listItemHeightSmall = 48.dp      // Small list item height
    val listItemHeightLarge = 88.dp      // Large list item height
}
